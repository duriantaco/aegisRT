@main.command()
@click.argument("path", default=".")
def audit(path: str) -> None:
    from aegisrt.audit.rules import ALL_RULES
    from aegisrt.audit.python_ast import parse_file

    console.print(f"[bold]Auditing[/bold] {path} for LLM security anti-patterns...\n")

    root = Path(path)
    all_findings = []
    scanned = 0

    if root.is_file():
        py_files = [root]
    else:
        py_files = sorted(root.rglob("*.py"))

    for py_file in py_files:
        parts = py_file.parts
        if any(
            p.startswith(".") or p in {"__pycache__", "node_modules", ".venv", "venv"}
            for p in parts
        ):
            continue

        tree = parse_file(py_file)
        if tree is None:
            continue
        scanned += 1

        for rule in ALL_RULES:
            findings = rule.match(tree, str(py_file))
            all_findings.extend(findings)

    if not all_findings:
        console.print(f"[green]No findings in {scanned} files scanned.[/green]")
        return

    table = Table(title="Audit Findings", show_header=True, header_style="bold cyan")
    table.add_column("Rule", style="bold")
    table.add_column("File", style="dim")
    table.add_column("Line", justify="right")
    table.add_column("Severity")
    table.add_column("Message", max_width=60)

    has_high_or_critical = False
    for finding in all_findings:
        sev = finding.severity.lower()
        sev_color = {
            "critical": "bold red",
            "high": "red",
            "medium": "yellow",
            "low": "green",
        }.get(sev, "white")

        if sev in ("high", "critical"):
            has_high_or_critical = True

        table.add_row(
            finding.rule_id,
            finding.file_path,
            str(finding.line),
            f"[{sev_color}]{sev.upper()}[/{sev_color}]",
            finding.message,
        )

    console.print(table)
    console.print(f"\n  Scanned files : {scanned}")
    console.print(f"  Total findings: {len(all_findings)}")

    if has_high_or_critical:
        console.print("\n[bold red]High/critical findings detected.[/bold red]")
        sys.exit(1)

