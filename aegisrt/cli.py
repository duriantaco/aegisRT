@benchmark_group.command(name="compare")
@click.argument("benchmark_id_1")
@click.argument("benchmark_id_2")
@click.option(
    "-d",
    "--dir",
    "output_dir",
    default=".aegisrt",
    help="Output directory where benchmarks are stored.",
)
def benchmark_compare(benchmark_id_1: str, benchmark_id_2: str, output_dir: str) -> None:
    import json as json_mod

    from aegisrt.core.benchmark import BenchmarkReport
    from aegisrt.reports.benchmark_report import BenchmarkTerminalReporter

    def _load(bid: str) -> BenchmarkReport | None:
        p = Path(output_dir) / "benchmarks" / bid / "benchmark.json"
        if not p.exists():
            return None
        data = json_mod.loads(p.read_text(encoding="utf-8"))
        return BenchmarkReport.model_validate(data)

    r1 = _load(benchmark_id_1)
    if r1 is None:
        console.print(f"[red]Benchmark not found:[/red] {benchmark_id_1}")
        sys.exit(1)

    r2 = _load(benchmark_id_2)
    if r2 is None:
        console.print(f"[red]Benchmark not found:[/red] {benchmark_id_2}")
        sys.exit(1)

    console.print(f"[bold]Comparing benchmarks[/bold]\n")
    console.print(f"  [cyan]A[/cyan]: {benchmark_id_1} ({r1.timestamp})")
    console.print(f"  [cyan]B[/cyan]: {benchmark_id_2} ({r2.timestamp})")
    console.print()

    console.print("[bold]--- Benchmark A ---[/bold]")
    BenchmarkTerminalReporter(console).report(r1)
    console.print()
    console.print("[bold]--- Benchmark B ---[/bold]")
    BenchmarkTerminalReporter(console).report(r2)

    console.print()
    table = Table(title="Delta (B - A)", show_header=True, header_style="bold cyan")
    table.add_column("Target", style="bold")
    table.add_column("A Rate", justify="right")
    table.add_column("B Rate", justify="right")
    table.add_column("Delta", justify="right")

    a_scores = {s.target_name: s for s in r1.scores}
    b_scores = {s.target_name: s for s in r2.scores}
    all_targets = sorted(set(a_scores.keys()) | set(b_scores.keys()))

    for name in all_targets:
        a_rate = a_scores[name].pass_rate if name in a_scores else 0.0
        b_rate = b_scores[name].pass_rate if name in b_scores else 0.0
        delta = (b_rate - a_rate) * 100
        color = "green" if delta > 0 else ("red" if delta < 0 else "dim")
        sign = "+" if delta > 0 else ""
        table.add_row(
            name,
            f"{a_rate * 100:.1f}%",
            f"{b_rate * 100:.1f}%",
            f"[{color}]{sign}{delta:.1f}pp[/{color}]",
        )

    console.print(table)

@benchmark_group.command(name="leaderboard")
@click.option(
    "-d",
    "--dir",
    "output_dir",
    default=".aegisrt",
    help="Output directory where benchmarks are stored.",
)
def benchmark_leaderboard(output_dir: str) -> None:
    import json as json_mod

    from aegisrt.core.benchmark import BenchmarkReport

    bench_dir = Path(output_dir) / "benchmarks"
    if not bench_dir.exists():
        console.print("[yellow]No benchmarks found.[/yellow]")
        return

    all_scores: dict[str, list[float]] = {}

    for bdir in sorted(bench_dir.iterdir()):
        jpath = bdir / "benchmark.json"
        if not jpath.exists():
            continue
        try:
            data = json_mod.loads(jpath.read_text(encoding="utf-8"))
            report = BenchmarkReport.model_validate(data)
        except Exception:
            continue
        for score in report.scores:
            if score.target_name not in all_scores:
                all_scores[score.target_name] = []
            all_scores[score.target_name].append(score.pass_rate)

    if not all_scores:
        console.print("[yellow]No benchmark results found.[/yellow]")
        return

    table = Table(title="All-Time Leaderboard", show_header=True, header_style="bold cyan")
    table.add_column("Rank", justify="center")
    table.add_column("Target", style="bold")
    table.add_column("Best Rate", justify="right")
    table.add_column("Avg Rate", justify="right")
    table.add_column("Runs", justify="right")

    ranked = sorted(
        all_scores.items(),
        key=lambda kv: max(kv[1]),
        reverse=True,
    )

    for i, (name, rates) in enumerate(ranked, 1):
        best = max(rates) * 100
        avg = (sum(rates) / len(rates)) * 100
        table.add_row(
            str(i),
            name,
            f"{best:.1f}%",
            f"{avg:.1f}%",
            str(len(rates)),
        )

    console.print(table)

