def _build_config(preset_name: str, model: str, profile: str, judge: bool) -> str:
    """Build an aegisrt.yaml string from preset + model + profile + judge flag."""
    p = _PRESETS[preset_name]
    probes = _PROFILES[profile]
    detectors = "[llm_judge]" if judge else "[regex, policy]"

    lines = [
        f"# Generated by: aegisrt init --preset {preset_name} --model {model} --profile {profile}"
        + (" --judge" if judge else ""),
        "",
        "target:",
        f"  type: {p['target_type']}",
        f'  url: "{p["url"]}"',
        "  timeout_seconds: 30",
        "  retries: 2",
    ]

    if p["headers"]:
        lines.append("  headers:")
        lines.append(p["headers"])

    lines.extend([
        "  params:",
        f'    model: "{model}"',
        '    max_tokens: "1024"',
        "",
        "probes:",
    ])

    for probe_id, family, severity, tags in probes:
        lines.extend([
            f"  - id: {probe_id}",
            f"    family: {family}",
            "    generator: static",
            f"    detectors: {detectors}",
            f"    severity: {severity}",
            f"    tags: {tags}",
            "",
        ])

    if judge and p["judge_type"]:
        lines.extend([
            "providers:",
            "  judge:",
            f"    type: {p['judge_type']}",
            f'    model: "{p["judge_model"]}"',
            f'    base_url: "{p["judge_base_url"]}"',
            f'    api_key: "{p["judge_api_key"]}"',
            "",
        ])

    lines.extend([
        "runtime:",
        "  concurrency: 3",
        "  retries: 1",
        "  timeout_seconds: 60",
        "",
        "report:",
        "  formats: [terminal, json]",
        "  output_dir: .aegisrt",
        "  fail_on:",
        "    severity: high",
        "    min_confidence: 0.7",
        "",
    ])

    return "\n".join(lines)


def _tty_prompt() -> tuple[str, str, str, bool]:
    """Ask 2-4 short questions when running in a terminal."""
    console.print()
    console.print("[bold]What are you testing?[/bold]")
    console.print("  1. Claude (Anthropic)")
    console.print("  2. GPT (OpenAI)")
    console.print("  3. Local model (Ollama, vLLM, LM Studio)")
    console.print()

    preset_choice = click.prompt("Choose", type=click.IntRange(1, 3), default=1)
    preset_map = {1: "anthropic", 2: "openai", 3: "ollama"}
    preset = preset_map[preset_choice]
    default_model = _PRESETS[preset]["default_model"]

    model = click.prompt("Model", default=default_model)

    console.print()
    console.print("[bold]How thorough?[/bold]")
    console.print("  1. Quick  — 4 probes, fast feedback")
    console.print("  2. Standard — 10 probes, covers OWASP Top 10")
    console.print("  3. Full — 13 probes, includes CBRN/cyber/persuasion")
    console.print()

    profile_choice = click.prompt("Choose", type=click.IntRange(1, 3), default=1)
    profile_map = {1: "quick", 2: "standard", 3: "full"}
    profile = profile_map[profile_choice]

    judge = False
    if preset != "ollama":
        judge = click.confirm(
            "Use LLM-as-judge for accurate grading? (uses extra API calls)", default=True
        )

    return preset, model, profile, judge

